public class Main {
    public static void main(String[] args) {
    // Bucle While
        int i = 10; // iterador, el valor inicial del bucle

        //inicializar el bucle
        while (i > 0) {
            System.out.println("El valor es:" + i); //Concatenar, juntar la frase con el valor que tenga la variable
            i--; //Decremento
        }
    }
}